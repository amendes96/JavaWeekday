package practice;

public class ForLoop {

	public static void main(String[] args) {
		//for loop
		//increment
		for(int x=0; x<10; x++) {
			System.out.println("Increment/ascending "+x);
		}System.out.println("---------");
	
		//decrement
		for(int i=10; i>0; i=i-1) {
			System.out.println("Descending/decrement " +i);
		}System.out.println("----------");
		
		for(int j=1; j<11; j++) {
			System.out.println("I love learning Java "+j);
		}
	}

}
